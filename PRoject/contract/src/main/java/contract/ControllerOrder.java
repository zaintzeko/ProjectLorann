/**
 * The Enum ControllerOrder.
 *
 * @author
 */
public enum ControllerOrder {

	/** no movement */
	NOMOUVEMENT,
	/** up right movement */
	RIGHTUP,
	/** up left movement */
	LEFTUP,
	/** down right movement */
	RIGHTDOWN,
	/** down left movement */
	LEFTDOWN,
	/** up movement*/
	UP,
	/** right movement*/
	RIGHT,
	/** left movement*/
	LEFT,
	/** down movement*/
	DOWN



}
